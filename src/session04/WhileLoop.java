package session04;

public class WhileLoop {

	public static void main(String[] args) 
	{
       int i=1;
       
       while(i<10)
       {
    	   System.out.println("Value of i: "+i);
    	   i++;//i+1
       }
        
       System.out.println("while loop exits. value of i is :"+i );
		
	}

}
